    public boolean checkexistence(ManipulateStudents s, String regno)
    {
        boolean flag=true;
        for(int j=0 ; j<s.student.size() ; j++)
        {
            if(regno.equals(s.student.get(j).getId()))
            {
                flag=false;
            }
        }
        return flag;
    }
    
    public int checkRubric(ManipulateRubrics r, String a, String c)
    {
        int count=0;
       for(int i=0 ; i<r.Rubriclist.size(); i++){
           
        if(a.equals(r.Rubriclist.get(i).getAssno()) && c.equals(r.Rubriclist.get(i).getCLONO()))
        {
            count++;
            break;
        }
        }
       return count;
    }

