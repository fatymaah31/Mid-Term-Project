package rubric;

/**
 *
 * @author fatym
 */
public class CLOs {
    private String coursetitle;
    private String coursecode;
    private String clo;  
    private String plo;
    private String clodes;

    /**
     * @return the coursetitle
     */
    public String getCoursetitle() {
        return coursetitle;
    }

    /**
     * @param coursetitle the coursetitle to set
     */
    public void setCoursetitle(String coursetitle) {
        this.coursetitle = coursetitle;
    }

    /**
     * @return the coursecode
     */
    public String getCoursecode() {
        return coursecode;
    }

    /**
     * @param coursecode the coursecode to set
     */
    public void setCoursecode(String coursecode) {
        this.coursecode = coursecode;
    }

    /**
     * @return the clo
     */
    public String getClo() {
        return clo;
    }

    /**
     * @param clo the clo to set
     */
    public void setClo(String clo) {
        this.clo = clo;
    }

    /**
     * @return the plo
     */
    public String getPlo() {
        return plo;
    }

    /**
     * @param plo the plo to set
     */
    public void setPlo(String plo) {
        this.plo = plo;
    }

    /**
     * @return the clodes
     */
    public String getClodes() {
        return clodes;
    }

    /**
     * @param clodes the clodes to set
     */
    public void setClodes(String clodes) {
        this.clodes = clodes;
    }
    

   
    
    
}
